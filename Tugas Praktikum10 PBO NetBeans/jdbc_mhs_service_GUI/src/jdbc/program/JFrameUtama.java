    private void jButtonTambahkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTambahkanActionPerformed
        // TODO add your handling code here:
        String nama = jTextFieldnama.getText().trim();
        
        if (nama.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "‚ö†Ô∏è Nama tidak boleh kosong!", 
                "Warning", 
                JOptionPane.WARNING_MESSAGE);
            jTextFieldnama.requestFocus();
            return;
        }
        
        try {
            Mahasiswa mhs = new Mahasiswa(nama);
            service.add(mhs);
            
            // Refresh data
            loadDataToList();
            
            // Clear input
            jTextFieldnama.setText("");
            
            JOptionPane.showMessageDialog(this, 
                "‚úÖ Data mahasiswa '" + nama + "' berhasil ditambahkan!", 
                "Success", 
                JOptionPane.INFORMATION_MESSAGE);
                
        } catch (Exception e) {
            System.err.println("‚ùå Error saat tambah data: " + e.getMessage());
            JOptionPane.showMessageDialog(this, 
                "‚ùå Error saat menambah data: " + e.getMessage(), 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonTambahkanActionPerformed

    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        //TODO add your handling code here:
        String idText = jTextField2.getText().trim();
        String namaBaru = jTextField4.getText().trim();
        
        if (idText.isEmpty() || namaBaru.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "‚ö†Ô∏è ID dan Nama harus diisi!", 
                "Warning", 
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try {
            int id = Integer.parseInt(idText);
            
            Mahasiswa mhs = service.getById(id);
            if (mhs == null) {
                JOptionPane.showMessageDialog(this, 
                    "‚ùå Data dengan ID " + id + " tidak ditemukan!", 
                    "Error", 
                    JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            String namaLama = mhs.getNama();
            mhs.setNama(namaBaru);
            service.update(mhs);
            
            // Refresh data
            loadDataToList();
            
            // Clear inputs
            jTextField2.setText("");
            jTextField4.setText("");
            
            JOptionPane.showMessageDialog(this, 
                "‚úÖ Data berhasil diupdate!\nNama lama: " + namaLama + "\nNama baru: " + namaBaru, 
                "Success", 
                JOptionPane.INFORMATION_MESSAGE);
                
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, 
                "‚ùå ID harus berupa angka!", 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
            jTextField2.requestFocus();
        } catch (Exception e) {
            System.err.println("‚ùå Error saat update: " + e.getMessage());
            JOptionPane.showMessageDialog(this, 
                "‚ùå Error saat update data: " + e.getMessage(), 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonEditActionPerformed

    private void jButtonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHapusActionPerformed
        // TODO add your handling code here:
        String idText = jTextField3.getText().trim();
        
        if (idText.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "‚ö†Ô∏è ID harus diisi!", 
                "Warning", 
                JOptionPane.WARNING_MESSAGE);
            jTextField3.requestFocus();
            return;
        }
        
        try {
            int id = Integer.parseInt(idText);
            
            // Cek apakah mahasiswa dengan ID tersebut ada
            Mahasiswa mahasiswa = service.getById(id);
            if (mahasiswa == null) {
                JOptionPane.showMessageDialog(this, 
                    "‚ùå Mahasiswa dengan ID " + id + " tidak ditemukan!", 
                    "Error", 
                    JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            String nama = mahasiswa.getNama();
            
            int confirm = JOptionPane.showConfirmDialog(this, 
                "‚ö†Ô∏è Apakah Anda yakin ingin menghapus:\nID: " + id + "\nNama: " + nama + "?", 
                "Konfirmasi Hapus", 
                JOptionPane.YES_NO_OPTION);
            
            if (confirm == JOptionPane.YES_OPTION) {
                service.delete(id);
                
                // Refresh data
                loadDataToList();
                
                // Clear inputs
                jTextField3.setText("");
                jTextField5.setText("");
                
                JOptionPane.showMessageDialog(this, 
                    "‚úÖ Data mahasiswa '" + nama + "' berhasil dihapus!", 
                    "Success", 
                    JOptionPane.INFORMATION_MESSAGE);
            }
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, 
                "‚ùå ID harus berupa angka!", 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
            jTextField3.requestFocus();
        } catch (Exception e) {
            System.err.println("‚ùå Error saat hapus: " + e.getMessage());
            JOptionPane.showMessageDialog(this, 
                "‚ùå Error saat menghapus data: " + e.getMessage(), 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonHapusActionPerformed

